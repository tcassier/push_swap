#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	size_t	index;

	index = 0;
	while (index < n)
	{
		if (((char*)s)[index] == (char)c)
			return ((void*)s + index);
		index++;
	}
	return (NULL);
}
